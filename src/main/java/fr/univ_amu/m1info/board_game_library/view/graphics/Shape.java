package fr.univ_amu.m1info.board_game_library.view.graphics;

/**
 * Enum representing different shapes that can be used in a board game.
 * Each shape can be displayed on the game board to represent various game elements.
 */
public enum Shape {

    /** A square shape. */
    SQUARE,

    /** A circular shape. */
    CIRCLE,

    /** A diamond shape. */
    DIAMOND,

    /** A star-shaped figure. */
    STAR,

    /** A triangular shape. */
    TRIANGLE,
}